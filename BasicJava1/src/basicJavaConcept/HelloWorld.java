package basicJavaConcept;

public class HelloWorld {

	public static void main(String [] args){
		System.out.println("Hello World!, This is Print Statement");
		
		//this is New Line
		System.out.println("Hello World!"+"\n"+"This is Print Statement");
		
		//This is new line
		System.out.println("This is First line"+"\r"+"This is Second Line");
		
		//This is new line
		System.out.println("This is Third line"+"\r\n"+"This is Fourth Line");
		//Double tab Space
		System.out.println("Hello World!" + "\t\t" + "This is Print Statement");
		//Single tab Space
		System.out.println("Hello World!" + "\t"+ "This is Print Statement");
	}
}
